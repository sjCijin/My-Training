package newone;

public class Newone {
	static int a=10;
	static int b=20;
	
	public static void add()
	{
		int c=a+b;
		System.out.println(c);
		
	}
	public static void sub()
	{
		int d=a-b;
		System.out.println(d);
	}
	public static void multi()
	{
		int d=a*b;
		System.out.println(d);

	}
	

	public static void main(String[] args) {
		add();
		sub();
		multi();
	}

}
