package newone;

public class NewSwitch {

	public static void main(String[] args) {
		int num=2;
		switch(num)
		{
		case 0:
			System.out.println("Number is 1");
			break;
		case 1:
			System.out.println("Number is 2");
			break;
		case 2:
			System.out.println("Number is 0");
			break;
			default:
				System.out.println("Not a num");
				break;
		 
		}
	

	}

}
